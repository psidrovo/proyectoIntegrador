    private void btnActualizarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnActualizarActionPerformed
        Usuario us = new Usuario();
        us.setCorreo(txtCorreo.getText());
        us.setPassword(txtPassword.getText());
        us.setTipo("USUARIO");
        if (btnActualizar.getText().equals("CREAR")) {
            us.crearUsuario(us);
        } else {
            us.setId(Integer.parseInt(lblId.getText()));
            us.actualizarUsuario(us);
        }
    }//GEN-LAST:event_btnActualizarActionPerformed

    private void btnEditarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEditarActionPerformed
        btnActualizar.setText("ACTUALIZAR");
        ExpresionRegular ex = new ExpresionRegular();
        ex.ingresarRegex("\\d+");
        if (ex.validar(cmbUsuarios.getSelectedItem().toString())) {
            lblId.setText(ex.obtenerId(cmbUsuarios.getSelectedItem().toString()) + "");
            Usuario us = new Usuario();
            us.extraerDatos(ex.obtenerId(cmbUsuarios.getSelectedItem().toString()));
            txtCorreo.setText(us.getCorreo());
            txtPassword.setText(us.getPassword());
        }
    }//GEN-LAST:event_btnEditarActionPerformed


